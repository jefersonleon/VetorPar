       VetorPar v = new VetorPar();
    int i = 0;
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (i >= 0 && i < v.getSize()) {
            v.setNumero(i, Integer.parseInt(txtValor.getText()));
            txtValor.setText(null);
            txtValor.requestFocus();
            i++;
            if (i == v.getSize()) {
                JOptionPane.showMessageDialog(null, "O seu vetor chegou no limite");
                btnAdd.setEnabled(false);
                txtResp.setText(v.toString() + "\n " + v.contarPar());
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        txtResp.setText(null);
        i = 0;
        btnAdd.setEnabled(true);
        v.mudarVetor(Integer.parseInt(JOptionPane.showInputDialog("Qual serÃ¡ o novo tamanho do vetor?")));
    }//GEN-LAST:event_btnLimparActionPerformed

